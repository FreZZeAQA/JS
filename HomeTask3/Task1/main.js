//- За допомогою циклу for і document.write() вивести 10 блоків div
// c довільним текстом всередині і індексом всередині

for (let i = 0; i < 10; i++) {
    document.write(`<div> ${i} Hello OktenWeb</div>`)
}

//- За допомогою циклу while вивести в документ 20 блоків h1
// c довільним текстом і індексом всередині.


let i = 0;
while (i < 20) {
    document.write(`<h1> ${i+1}Hello OktenWeb</h1>`)
    i++;
}




